# ============================================================
# Solvent LBM helpers
# ============================================================
def equilibrium(rho, u):
    cu = torch.einsum('ic,cxy->ixy', c, u)
    u_sq = 0.5 * (u[0]**2 + u[1]**2)
    return w.view(9, 1, 1) * rho.unsqueeze(0) * (
        1.0 + 3.0 * cu + 4.5 * cu**2 - 3.0 * u_sq.unsqueeze(0)
    )

def stream(f):
    out = torch.zeros_like(f)
    for i in range(9):
        out[i] = torch.roll(f[i], shifts=(int(c[i, 0]), int(c[i, 1])), dims=(0, 1))
    return out

def bounceback(f, kappa):
    return (1.0 - kappa).unsqueeze(0) * f + kappa.unsqueeze(0) * f[opposite]

# ============================================================
# Non-periodic helpers for pigment transport
# ============================================================
def pad_rep(field):
    return F.pad(field.unsqueeze(0).unsqueeze(0), (1, 1, 1, 1), mode='replicate')[0, 0]

def grad(field):
    f = pad_rep(field)
    gx = 0.5 * (f[2:, 1:-1] - f[:-2, 1:-1])
    gy = 0.5 * (f[1:-1, 2:] - f[1:-1, :-2])
    return gx, gy

def laplacian(field):
    f = pad_rep(field)
    return (
        f[2:, 1:-1] + f[:-2, 1:-1] +
        f[1:-1, 2:] + f[1:-1, :-2] -
        4.0 * f[1:-1, 1:-1]
    )

def advect_upwind(m, ux, uy):
    f = pad_rep(m)

    m_xp = f[2:, 1:-1]
    m_xm = f[:-2, 1:-1]
    m_yp = f[1:-1, 2:]
    m_ym = f[1:-1, :-2]

    dm_dx = torch.where(ux > 0, m - m_xm, m_xp - m)
    dm_dy = torch.where(uy > 0, m - m_ym, m_yp - m)

    return m - ux * dm_dx - uy * dm_dy

# ============================================================
# Evaporation
# ============================================================
def compute_evaporation_laplace(rho, rho0, kappa, evap_coeff, pin_mask, c_sat=1.0, c_amb=0.3, iters=60):
    excess = (rho - rho0).clamp(min=0)
    wet = excess > 0.003

    if wet.sum() < 5:
        return torch.zeros_like(rho), wet

    c_vapor = torch.ones_like(rho) * c_amb
    c_vapor[wet] = c_sat
    wet_f = wet.float()

    for _ in range(iters):
        c_new = 0.25 * (
            torch.roll(c_vapor, 1, 0) + torch.roll(c_vapor, -1, 0) +
            torch.roll(c_vapor, 1, 1) + torch.roll(c_vapor, -1, 1)
        )
        c_new = c_new * (1.0 - wet_f) + c_sat * wet_f
        c_vapor = c_new

    gx, gy = grad(c_vapor)
    J = torch.sqrt(gx**2 + gy**2)

    pore_factor = 1.0 - 0.35 * kappa
    rim_boost = 1.0 + 1.2 * pin_mask
    J = evap_coeff * J * wet_f * pore_factor * rim_boost
    J = torch.minimum(J, 0.08 * excess)
    return J, wet

# ============================================================
# Pigment transport
# ============================================================
def advect_diffuse_phi(phi, deposit, rho, u, J, kappa, pin_active):
    h = (rho - rho0).clamp(min=0)
    wet = h > 1e-8
    wet_f = wet.float()

    # mobile pigment mass
    m = phi * h

    # lower mobility in dense fiber regions
    drag = 0.1 - 0.1 * kappa

    # ------------------------------------------------------------------
    # Depth-averaged film velocity from mass conservation.
    # Contact line is pinned, so fluid removed by evaporation must be
    # replaced by in-plane flow: grad . (h u_film) = -J.  Writing
    # h u_film = grad(psi) gives Poisson:  laplacian(psi) = -J,
    # with psi = 0 on the dry region so no flux crosses the contact line.
    # Solving by Jacobi iteration using the pad_rep laplacian stencil.
    # ------------------------------------------------------------------
    psi = torch.zeros_like(h)
    src = -J * wet_f
    for _ in range(40):
        p = pad_rep(psi)
        psi_new = 0.25 * (
            p[2:, 1:-1] + p[:-2, 1:-1] +
            p[1:-1, 2:] + p[1:-1, :-2] - src
        )
        psi = psi_new * wet_f  # enforce psi=0 outside wet region

    gx_psi, gy_psi = grad(psi)
    inv_h = wet_f / (h + 1e-6)
    u_film_x = gx_psi * inv_h
    u_film_y = gy_psi * inv_h

    # stability cap
    u_film_x = torch.clamp(u_film_x, -0.08, 0.08)
    u_film_y = torch.clamp(u_film_y, -0.08, 0.08)

    ux = u[0] * drag + u_film_x
    uy = u[1] * drag + u_film_y

    # transport
    m_adv = advect_upwind(m, ux, uy)
    m_adv = m_adv + D_phi * laplacian(m)
    m_adv = torch.clamp(m_adv, min=0.0)

    # deposition strongly biased toward strong evaporation zones
    thin_factor = torch.clamp((thin_film_thresh - h) / thin_film_thresh, 0.0, 1.0)
    fiber_factor = 0.8 + 0.2 * kappa

    dep_prob = deposit_rate * (thin_factor ** 2) * fiber_factor * pin_active
    dep_prob = torch.clamp(dep_prob, 0.0, 0.25)

    dm_dep = dep_prob * m_adv * wet.float()
    m_mobile = m_adv - dm_dep
    deposit = deposit + dm_dep

    phi_new = torch.zeros_like(phi)
    phi_new[wet] = m_mobile[wet] / (h[wet] + 1e-10)
    phi_new = torch.clamp(phi_new, 0.0, max_phi)

    return phi_new, deposit

# ============================================================
# Diagnostics
# ============================================================
def radial_profile(field, cx, cy, n_bins=120):
    yy, xx = torch.meshgrid(
        torch.arange(field.shape[0], device=field.device),
        torch.arange(field.shape[1], device=field.device),
        indexing='ij'
    )
    rr = torch.sqrt((xx - cx)**2 + (yy - cy)**2)
    rr_i = torch.clamp(rr.long(), 0, n_bins - 1)

    sums = torch.zeros(n_bins, device=field.device)
    counts = torch.zeros(n_bins, device=field.device)

    sums.scatter_add_(0, rr_i.flatten(), field.flatten())
    counts.scatter_add_(0, rr_i.flatten(), torch.ones_like(field).flatten())

    return (sums / (counts + 1e-10)).detach().cpu().numpy()

