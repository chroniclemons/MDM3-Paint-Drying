# Geometry
# ============================================================
Y, X = torch.meshgrid(
    torch.arange(Nx, device=device, dtype=torch.float32),
    torch.arange(Ny, device=device, dtype=torch.float32),
    indexing='ij'
)

cx0, cy0 = Nx // 2, Ny // 2
dx = X - cy0
dy = Y - cx0
theta = torch.atan2(dy, dx)
r = torch.sqrt(dx**2 + dy**2)

# --- uneven blob radius as a function of angle ---
base_radius = 52.0
r_blob = (
    base_radius
    + 1.0 * torch.sin(3.0 * theta)
    + 2.0 * torch.sin(7.0 * theta + 0.8)
    + 1.5 * torch.cos(5.0 * theta - 0.4)
)

inside = r < r_blob

rho = torch.ones((Nx, Ny), device=device) * rho0
rho[inside] = rho_drop

u = torch.zeros((2, Nx, Ny), device=device)
f = equilibrium(rho, u)

deposit = torch.zeros((Nx, Ny), device=device)

# --- blob-shaped pin: same angular profile as r_blob, scaled to pin_radius ---
r_pin_blob = r_blob * (pin_radius / base_radius)

pin_mask = ((r > r_pin_blob - pin_band) & (r < r_pin_blob + pin_band)).float()

# Hard pin: fixed wet domain follows the blob shape
wet_domain = (r <= r_pin_blob).float()
pin_wall = ((r > r_pin_blob) & (r <= r_pin_blob + 2)).float()  # 2-cell shell

# ============================================================
# Stage 1 — solvent only
# ============================================================
snapshots = []
profiles = []
stage1_solvent_snaps = {}

# Animation frames
anim_stride = 25
stage1_frames = []
stage2_frames = []

print(f"Stage 1: running {steps} solvent steps...")
for step in range(steps):
    f = torch.nan_to_num(f, nan=0.0, posinf=0.0, neginf=0.0)
    f = torch.clamp(f, min=0.0)
    rho = f.sum(dim=0).clamp(min=1e-6)
    momentum = torch.einsum('ic,ixy->cxy', c, f)
    u = momentum / (rho.unsqueeze(0) + 1e-10)

    # pin activation: zero during spreading phase, ramps to 1 over 50 steps
    pin_active = max(0.0, min(1.0, (step - spread_steps) / 50.0))
    active_pin_mask = pin_mask * pin_active

    # hard pin: bounce-back wall on the ring of Ω
    kappa_eff = torch.clamp(kappa + pin_active * pin_wall, 0.0, 1.0)

    u_forced = torch.clamp(u, -0.1, 0.1)

    # solvent collide/stream
    feq = equilibrium(rho, u_forced)
    f = f - omega * (f - feq)
    f = bounceback(f, kappa_eff)
    f = stream(f)

    # damp velocity near the wall to kill inward reflections
    if pin_active > 0.5:
        damp_band = ((r > r_pin_blob - 3) & (r <= r_pin_blob)).float()
        rho_local = f.sum(dim=0)
        u_local = torch.einsum('ic,ixy->cxy', c, f) / (rho_local.unsqueeze(0) + 1e-10)
        u_damped = u_local * (1.0 - 0.8 * damp_band).unsqueeze(0)
        f_relax = equilibrium(rho_local, u_damped)
        f = f * (1.0 - damp_band).unsqueeze(0) + f_relax * damp_band.unsqueeze(0)

    # evaporation
    rho_post = f.sum(dim=0)
    J, wet = compute_evaporation_laplace(
        rho_post, rho0, kappa, evap_coeff, active_pin_mask,
        c_sat=c_sat, c_amb=c_amb, iters=laplace_iters
    )
    excess = (rho_post - rho0).clamp(min=1e-10)
    evap_frac = torch.clamp(J / excess, 0.0, 0.20)
    f = f - evap_frac.unsqueeze(0) * (
        f - equilibrium(torch.ones_like(rho_post) * rho0, torch.zeros_like(u))
    )

    if step % 500 == 0:
        h_dbg = (f.sum(dim=0) - rho0).clamp(min=0)
        print(f"  stage1 step {step:4d} | solvent excess={h_dbg.sum().item():10.3f}")

    if step in snap_at:
        h_s1 = (f.sum(dim=0) - rho0).clamp(min=0)
        stage1_solvent_snaps[step] = h_s1.detach().cpu().numpy().copy()

    if step % anim_stride == 0:
        h_anim = (f.sum(dim=0) - rho0).clamp(min=0)
        stage1_frames.append(h_anim.detach().cpu().numpy().copy())

# freeze the final shape
rho_final = f.sum(dim=0)
h_final = (rho_final - rho0).clamp(min=0)
footprint = (h_final > 1e-3).float()
print(f"Stage 1 done. Footprint cells: {int(footprint.sum().item())}")

# ============================================================
# Stage 2 — pigment on the frozen shape
# ============================================================
phi = torch.zeros((Nx, Ny), device=device)
phi[footprint > 0.5] = phi_init
deposit = torch.zeros((Nx, Ny), device=device)

# Frozen solvent that the pigment code will see.
rho_frozen = rho0 + h_final
u_frozen = torch.zeros((2, Nx, Ny), device=device)

stage2_steps = steps
stage2_snap_at = snap_at

print(f"Stage 2: running {stage2_steps} pigment steps on frozen shape...")

# Rescale stage 1's final shape so its peak matches a fresh drop.
# This preserves the relative density gradient from stage 1 but gives
# the pigment code a full-height film to work with.
h0_stage2 = rho_drop - rho0   # 0.20, same as the original drop peak
h_final_max = h_final.max().clamp(min=1e-6)
h_final_scaled = h_final * (h0_stage2 / h_final_max)

# soften the edge so deposition doesn't concentrate on a cliff
for _ in range(8):
    p = pad_rep(h_final_scaled)
    h_final_scaled = 0.25 * (p[2:,1:-1] + p[:-2,1:-1] + p[1:-1,2:] + p[1:-1,:-2])

# thin ring at the edge of the smoothed shape, for rim evaporation boost
edge_mask = ((h_final_scaled > 0.02).float() - (h_final_scaled > 0.05).float()).clamp(min=0)

print(f"  rescaled h_final: old max={h_final_max.item():.4f} -> new max={h_final_scaled.max().item():.4f}")

for step in range(stage2_steps):
    # nonlinear thinning: fast at first, slower later — crosses the deposition
    # threshold around the middle of the run so deposition has time to build up
    t = step / stage2_steps
    decay = max(0.05, (1.0 - t) ** 0.6)
    h_t = h_final_scaled * decay
    rho_frozen_t = rho0 + h_t

    # live evaporation on the thinning shape — drives u_film in advect_diffuse_phi
    J, wet = compute_evaporation_laplace(
        rho_frozen_t, rho0, kappa, evap_coeff, edge_mask,
        c_sat=c_sat, c_amb=c_amb, iters=laplace_iters
    )

    # pigment transport
    phi, deposit = advect_diffuse_phi(
        phi, deposit, rho_frozen_t, u_frozen, J, kappa, pin_active=1.0
    )

    if step % anim_stride == 0:
        stage2_frames.append({
            "h": h_t.detach().cpu().numpy().copy(),
            "deposit": deposit.detach().cpu().numpy().copy(),
        })

    if step in stage2_snap_at:
        mobile_pigment = phi * h_t

        snapshots.append({
            "step": step,
            "solvent": stage1_solvent_snaps.get(step, h_final.detach().cpu().numpy().copy()),
            "mobile": mobile_pigment.detach().cpu().numpy().copy(),
            "deposit": deposit.detach().cpu().numpy().copy(),
            "J": J.detach().cpu().numpy().copy(),
        })

        profiles.append({
            "step": step,
            "mobile": radial_profile(mobile_pigment, cx0, cy0),
            "deposit": radial_profile(deposit, cx0, cy0),
        })

        print(
            f"  stage2 step {step:4d} | "
            f"mobile pigment={mobile_pigment.sum().item():10.3f} | "
            f"deposit={deposit.sum().item():10.3f}"
        )

print("Done.")

