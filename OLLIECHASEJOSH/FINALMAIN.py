import matplotlib.animation as animation

print("\nBuilding animation...")

# colormaps & scales
vmax_solvent_anim = max(max((frm.max() for frm in stage1_frames), default=1e-3), 1e-3)
vmax_deposit_anim = max(
    max((frm["deposit"].max() for frm in stage2_frames), default=1e-4),
    1e-4,
)
h_final_np = h_final_scaled.detach().cpu().numpy()
vmax_h_final = max(h_final_np.max() * 0.9, 1e-3)

fig_anim, ax_anim = plt.subplots(figsize=(7, 7))
ax_anim.axis("off")

# initial frame: stage 1 first frame
solvent_im = ax_anim.imshow(
    stage1_frames[0], cmap="Blues", vmin=0, vmax=vmax_solvent_anim,
    interpolation="bilinear",
)
deposit_im = ax_anim.imshow(
    np.zeros_like(stage1_frames[0]), cmap=dep_cmap,
    vmin=0, vmax=vmax_deposit_anim, alpha=0.0, interpolation="bilinear",
)
title = ax_anim.set_title("Stage 1 — solvent step 0", fontsize=13, fontweight="bold")

n1 = len(stage1_frames)
n2 = len(stage2_frames)
total_frames = n1 + n2

def update(frame_idx):
    if frame_idx < n1:
        # stage 1: show solvent evolving, deposit hidden
        solvent_im.set_data(stage1_frames[frame_idx])
        solvent_im.set_clim(0, vmax_solvent_anim)
        deposit_im.set_alpha(0.0)
        title.set_text(f"Stage 1 — solvent step {frame_idx * anim_stride}")
    else:
        # stage 2: stage 1's final shape held constant, deposit building on top
        i2 = frame_idx - n1
        frm = stage2_frames[i2]
        solvent_im.set_data(h_final_np)
        solvent_im.set_clim(0, vmax_h_final)
        dep = frm["deposit"]
        dep_norm = dep / max(dep.max(), 1e-8)
        deposit_im.set_data(dep)
        deposit_im.set_alpha(np.clip(dep_norm * 1.1, 0, 1))
        title.set_text(f"Stage 2 — deposit step {i2 * anim_stride}")
    return solvent_im, deposit_im, title

ani = animation.FuncAnimation(
    fig_anim, update, frames=total_frames, interval=50, blit=False
)

